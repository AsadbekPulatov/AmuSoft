     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $id = Auth::id();
        $user = User::OrderBy('id', 'DESC')->where('id', $id)->first();

        return view('admin.users.index',[
            'user' => $user,
        ]);
    }

